# объявляем переменные с операциями
multiply = 15 * 3
divide = 15 / 3
integer_division = 15 // 2
exponentiation = 15 ** 3
# оформление через цикл для вывода всей информации сразу, с форматированием вывода
while True:
    print(f'{type(multiply)} результат = {multiply}')
    print(f'{type(divide)} результат = {divide}')
    print(f'{type(integer_division)} результат = {integer_division}')
    print(f'{type(exponentiation)} результат = {exponentiation}')
    break


